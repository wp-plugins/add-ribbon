			<h3>Installation</h3>
			<ol>
				<li>BACKUP everything before you install the plugin.</li>
				<li>Upload add-ribbon directory to the '/wp-content/plugins/' directory</li>
				<li>Activate the plugin through the 'Plugins' menu in WordPress</li>	
			</ol>
			<h3>Adding Ribbon using Shortcode</h3>
			<ol>
				<li>Shortcode for Sherk Add Ribbon is in the format of<br/>
				<b>[ribbon id="myribbon" padding="10" height="100" width="100" color="#000" bgcolor="#FFF" label="Featured" position="right"]<br/>This is a sample content<br/>[/ribbon]</b></li>
				<li>Copy the shortcode, paste it to the content text editor and update the values of your shortcode parameters depends on what you need.</li>
			</ol>
			<h3>Setting Ribbon for Image Content</h3>
			<ol>
				<li>This is a way on adding ribbon using image as your content instead of text.</li>
				<li>Set your padding into 0</li>
				<li>Set your ribbon width and height to the width and height of your image.</li>
				<li>Give the image a class value of 'imgribbon'</li>
				<li>Ex.<br/>
					<b>[ribbon id="sherkid" padding="0" width="190" height="190" bgcolor="#CCC" color="#233" label="Sample"]<br/>
					< img class="imgribbon" src="images/landscape.jpg" alt="" width="190" height="190" /><br/>
					[/ribbon]</b><br/></li>
			</ol>
				
			
			<h3>Parameters Available</h3>

				<h4><b>id</b></h4>
				Id of your ribbon, used in customizing ribbon styles<br/>
				default: ''<br/>
				option: <b>string</b><br/><br/>
				
				<h4><b>label</b></h4>
				Text label for your ribbon<br/>
				default: 'Label'<br/>
				option: <b>string</b><br/><br/>
				
				<h4><b>wider</b></h4>
				Wider ribbon<br/>
				default: 'false'<br/>
				option: <b>boolean</b><br/><br/>
				
				<h4><b>position</b></h4>
				Position of the label of your ribbon<br/>
				default: 'left'<br/>
				option: <b>left</b><br/>
						<b>right</b><br/><br/>
						
				<h4><b>padding</b></h4>
				Padding of your ribbon<br/>
				default: '10'<br/>
				option: <b>integer</b><br/><br/>

				<h4><b>width</b></h4>
				Width of your ribbon<br/>
				default: '200'<br/>
				option: <b>integer</b><br/><br/>
				
				<h4><b>height</b></h4>
				Height of your ribbon<br/>
				default: '200'<br/>
				option: <b>integer</b><br/><br/>
				
				<h4><b>color</b></h4>
				Color of your text label<br/>
				default: '#FFF'<br/>
				option: <b>Hex Color</b><br/><br/>
				
				<h4><b>bgcolor</b></h4>
				Background color of your text label<br/>
				default: '#000'<br/>
				option: <b>Hex Color</b><br/><br/>
				
